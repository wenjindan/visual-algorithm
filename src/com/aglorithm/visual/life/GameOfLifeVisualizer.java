    public GameOfLifeVisualizer(int size) {
        GameOfLife gameOfLife = new GameOfLife(size);

        //事件窗口创建，事件分发
        EventQueue.invokeLater(()->{
            GameOfLifeFrame frame = new GameOfLifeFrame(gameOfLife);
            new Thread(()->{
                while (frame.isDisplayable()){
                    frame.repaint();
                    AigoVisHelper.pause(20);
                    gameOfLife.nextLife();
                }
            }).start();
        });
    }

